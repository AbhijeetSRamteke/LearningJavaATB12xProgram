package Tasks;

public class Variable_Declare_Initialize
{
    public static void main(String[] args)
    {
        int number = 30;
        System.out.println("Number :" + number);

        double abc = 29.99;
        System.out.println("double :" + abc);

        float num = 12.5f;
        System.out.println("float :" + num);

        long numl = 123456789l;
        System.out.println("long number :" + numl);

        boolean name = true;
        System.out.println("boolean : " + name);

        char xyz = 'A';
        System.out.println("Character :" + xyz);

    }
}
