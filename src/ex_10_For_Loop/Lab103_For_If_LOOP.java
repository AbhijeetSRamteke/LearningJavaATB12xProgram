package ex_10_For_Loop;

public class Lab103_For_If_LOOP
{
    public static void main(String[] args)
    {
        for (int abc=0; abc<18; abc++)
        {
            if (abc>15)
            {
                System.out.println("abc");
            }
            else
            {
                System.out.println("def");
            }
        }
    }
}
